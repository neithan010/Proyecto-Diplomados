SELECT DISTINCT a.nombre_area
FROM intranet.area_conocimiento a
WHERE a.nombre_area = 'Innovación' OR
		a.nombre_area = 'Finanzas e Inversiones' OR
		a.nombre_area = 'Marketing y Ventas' OR
		a.nombre_area = 'Estrategia' OR
		a.nombre_area = 'Personas y Equipos' OR
		a.nombre_area = 'Operaciones y Logística' OR
		a.nombre_area = 'Dirección de Instituciones de Salud'